#include <stdio.h>
/**
 * add - add two integers
 * @: first unt
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
  return (a + b);
}

/**
 * div - divided two integers
 * @: first unt
 * @b: second int
 * Return: divison
 */
int div(int a, int b)
{
  return (a / b);
}

/**
 * mul - mul two integers
 * @: first unt
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
  return (a * b);
}

/**
 * mod - mod of two integers
 * @: first unt
 * @b: second int
 * Return: mod
 */
int mod(int a, int b)
{
  return (a % b);
}

/**
 * sub - subtract two integers
 * @: first unt
 * @b: second int
 * Return:  subtraction
 */
int sub(int a, int b)
{
  return (a - b);
}
